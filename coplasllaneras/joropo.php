					llanero, central y oriental. Igualmente en la región de 
					Guayana, centro-occidental y de los andes se encuentra en 
					cada una un tipo de joropo con características especificas 
					que identifican la región:</font></p>
					<p class="texto" align="justify" style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
					<p class="texto" align="justify" style="margin-top: 0; margin-bottom: 0">
