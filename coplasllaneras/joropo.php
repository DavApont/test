					<p class="texto" align="justify"><font face="Arial"><b>
					<font size="5">E</font></b>s una forma tradicional de música y baile que 
					identifica plenamente al venezolano. Antiguamente “joropo” 
					se refería a una fiesta y con el pasar del tiempo se 
					identifica más bien como una forma de música y baile.</font></p>
					<p class="texto" align="justify"><font face="Arial">El 
					Joropo es actualmente un símbolo emblema de identidad 
					nacional. Sus orígenes se remontan a mediados de 1700 cuando 
					el campesino venezolano prefirió utilizar el término 
					“joropo” en vez de “fandango” para referirse a fiestas y 
					reuniones sociales y familiares. Fandango es un término de 
					origen español, el cual identifica unos de los cantos y 
					bailes más populares dentro del flamenco. El joropo parece 
					haber tomado del fandango el sentido de la fiesta ó baile... 
					abandonando el galanteo amoroso propio del fandango y adopta 
					el asido de manos y los giros del vals... Así se conserva 
					hasta ahora.</font></p>
					<p class="texto" align="justify"><font face="Arial">El 
					joropo es caracterizado por ser tan mestizado como nosotros 
					mismos, así encontramos que lo rítmico de la melodía, el 
					acompañamiento de arpa y cuatro, y en la versificación 
					literaria observamos la presencia europea. En la melodía 
					independiente identificamos la presencia del negro y en la 
					estampa de las maracas identificamos la huella indígena.</font></p>
					<p class="texto" align="justify"><font face="Arial">El 
					joropo no solo es un estilo musical, también es baile y 
					danza, y representa además una fiesta popular, es un baile 
					alegre que divierte y reúne a sus participantes, en cada 
					zona geográfica toma su propia esencia, y desarrolla 
					diferentes paso y figuras en el baile, existiendo sin 
					embargo figuras básicas que los identifican.</font></p>
					<p class="texto" align="justify"><font face="Arial">Las 
					principales figuras del joropo son el valsiao, el escobillao 
					y el zapatiao. El primero se da en el inicio del baile, en 
					el vals las parejas se abrazan suavemente recorriendo el 
					espacio de baile en tres tiempos propios del vals, dan 
					vueltas rápidas en giros espirales. El siguiente, el 
					escobillao, es una figura donde los bailarines colocados de 
					frente mueven los pies a manera de cortos avances y 
					retrocesos como si estuvieran cepillando el suelo. El 
					tercero, el zapatiao, es una figura varonil y se realiza por 
					la pareja sin soltar el abrazo ó sueltas completamente como 
					es propio en los llanos, en esa figura el hombre hace sonar 
					sus pisadas del fuego mientras la mujer se limita a 
					escobillar.</font></p>
